interface TennisRatingOverlayProps {
  visible: boolean;
  onClose: () => void;
  onBack?: () => void;
  onContinue?: (rating: string) => void;
  currentStep?: number;
  totalSteps?: number;
  mode?: 'onboarding' | 'edit'; // Mode: onboarding (create) or edit (update)
  initialRating?: string; // Pre-selected rating ID for edit mode
  onSave?: (ratingId: string) => void; // Save callback for edit mode
}

interface Rating {
  id: string;
  score_value: number;
  display_label: string;
  description: string;
  skill_level: 'beginner' | 'intermediate' | 'advanced' | 'professional' | null;
}

/**
 * Maps NTRP score value to a translation key
 */
const getNtrpSkillLabelKey = (scoreValue: number): string => {
  const mapping: Record<number, string> = {
    1.5: 'onboarding.ratingStep.skillLevels.beginner1',
    2.0: 'onboarding.ratingStep.skillLevels.beginner2',
    2.5: 'onboarding.ratingStep.skillLevels.beginner3',
    3.0: 'onboarding.ratingStep.skillLevels.intermediate1',
    3.5: 'onboarding.ratingStep.skillLevels.intermediate2',
    4.0: 'onboarding.ratingStep.skillLevels.intermediate3',
    4.5: 'onboarding.ratingStep.skillLevels.advanced1',
    5.0: 'onboarding.ratingStep.skillLevels.advanced2',
    5.5: 'onboarding.ratingStep.skillLevels.advanced3',
    6.0: 'onboarding.ratingStep.skillLevels.professional',
  };
  return mapping[scoreValue] || '';
};

/**
 * Maps NTRP score value to a description translation key
 */
const getNtrpDescriptionKey = (scoreValue: number): string => {
  return `onboarding.ratingStep.ntrpDescriptions.${scoreValue.toFixed(1).replace('.', '_')}`;
};

export function TennisRatingActionSheet({ payload }: SheetProps<'tennis-rating'>) {
  const mode = payload?.mode || 'onboarding';
  const onClose = () => SheetManager.hide('tennis-rating');
  const onBack = payload?.onBack;
  const onContinue = payload?.onContinue;
  const onSave = payload?.onSave;
  const currentStep = payload?.currentStep || 1;
  const totalSteps = payload?.totalSteps || 8;
  const initialRating = payload?.initialRating;
  const { colors, isDark } = useThemeStyles();
  const { t } = useTranslation();
  const [selectedRating, setSelectedRating] = useState<string | null>(initialRating || null);
  const [ratings, setRatings] = useState<Rating[]>([]);
  const [isLoading, setIsLoading] = useState(false);
  const [isSaving, setIsSaving] = useState(false);

  // Load ratings from database when component mounts
  useEffect(() => {
    const loadRatings = async () => {
      setIsLoading(true);
      try {
        const { data, error } = await DatabaseService.RatingScore.getRatingScoresBySport(
          'tennis',
          'ntrp'
        );

        if (error || !data) {
          Logger.error('Failed to load tennis ratings', error as Error, {
            sport: 'tennis',
            system: 'ntrp',
          });
          Alert.alert(
            t('alerts.error' as TranslationKey),
            t('onboarding.validation.failedToLoadRatings' as TranslationKey)
          );
          return;
        }

        // Transform database data to match UI expectations
        const transformedRatings: Rating[] = data.map(rating => ({
          id: rating.id,
          score_value: rating.score_value,
          display_label: rating.display_label,
          description: rating.description || '',
          skill_level: rating.skill_level, // May be null in new schema
        }));

        setRatings(transformedRatings);
      } catch (error) {
        Logger.error('Unexpected error loading tennis ratings', error as Error);
        Alert.alert(
          t('alerts.error' as TranslationKey),
          t('onboarding.validation.unexpectedError' as TranslationKey)
        );
      } finally {
        setIsLoading(false);
      }
    };

    loadRatings();
  }, [t]);

  // Update selected rating when initialRating changes
  useEffect(() => {
    if (initialRating) {
      setSelectedRating(initialRating);
    }
  }, [initialRating]);

  // Helper function to get skill category from NTRP score value
  const getSkillCategory = (scoreValue: number): string => {
    if (scoreValue <= 2.5) return 'beginner';
    if (scoreValue <= 4.0) return 'intermediate';
    if (scoreValue <= 5.5) return 'advanced';
    return 'professional';
  };

  // Helper function to get icon based on skill level
  const getRatingIcon = (skillLevel: string): keyof typeof Ionicons.glyphMap => {
    if (skillLevel === 'beginner') return 'star-outline';
    if (skillLevel === 'intermediate') return 'star-half';
    if (skillLevel === 'advanced') return 'star';
    return 'trophy'; // professional
  };

  const handleContinue = async () => {
    if (!selectedRating || isSaving) return;

    mediumHaptic();

    // Edit mode: use the onSave callback
    if (mode === 'edit' && onSave) {
      onSave(selectedRating);
      SheetManager.hide('tennis-rating');
      return;
    }

    // Onboarding mode: save to database
    if (onContinue) {
      setIsSaving(true);
      try {
        // Get tennis sport ID
        const { data: tennisSport, error: sportError } =
          await SportService.getSportByName('tennis');

        if (sportError || !tennisSport) {
          Logger.error('Failed to fetch tennis sport', sportError as Error);
          setIsSaving(false);
          Alert.alert(
            t('alerts.error' as TranslationKey),
            t('onboarding.validation.failedToSaveRating' as TranslationKey),
            [{ text: t('common.ok' as TranslationKey) }]
          );
          return;
        }

        // Find the selected rating data
        const selectedRatingData = ratings.find(r => r.id === selectedRating);

        if (!selectedRatingData) {
          setIsSaving(false);
          Alert.alert(
            t('alerts.error' as TranslationKey),
            t('onboarding.validation.invalidRating' as TranslationKey)
          );
          return;
        }

        // Save rating to database
        const ratingData: OnboardingRating = {
          sport_id: tennisSport.id,
          sport_name: 'tennis',
          rating_system_code: 'ntrp',
          score_value: selectedRatingData.score_value,
          display_label: selectedRatingData.display_label,
        };

        const { error } = await OnboardingService.saveRatings([ratingData]);

        if (error) {
          Logger.error('Failed to save tennis rating', error as Error, { ratingData });
          setIsSaving(false);
          Alert.alert(
            t('alerts.error' as TranslationKey),
            t('onboarding.validation.failedToSaveRating' as TranslationKey),
            [{ text: t('common.ok' as TranslationKey) }]
          );
          return;
        }

        Logger.debug('tennis_rating_saved', { ratingData });
        onContinue?.(selectedRating);
      } catch (error) {
        Logger.error('Unexpected error saving tennis rating', error as Error);
        setIsSaving(false);
        Alert.alert(
          t('alerts.error' as TranslationKey),
          t('onboarding.validation.unexpectedError' as TranslationKey),
          [{ text: t('common.ok' as TranslationKey) }]
        );
      }
    }
  };

  return (
    <ActionSheet
      gestureEnabled
      containerStyle={[styles.sheetBackground, { backgroundColor: colors.card }]}
      indicatorStyle={[styles.handleIndicator, { backgroundColor: colors.border }]}
    >
      <View style={styles.modalContent}>
        {/* Header */}
        <View style={[styles.header, { borderBottomColor: colors.border }]}>
          <View style={styles.headerCenter}>
            <Text
              weight="semibold"
              size="lg"
              style={{ color: colors.text }}
              numberOfLines={1}
              ellipsizeMode="tail"
            >
              {mode === 'edit'
                ? t('onboarding.ratingOverlay.editTennisTitle' as TranslationKey)
                : t('onboarding.ratingOverlay.title' as TranslationKey)}
            </Text>
          </View>
          <TouchableOpacity onPress={onClose} style={styles.closeButton}>
            <Ionicons name="close" size={24} color={colors.textMuted} />
          </TouchableOpacity>
        </View>

        {/* Scrollable Content */}
        <ScrollView
          style={styles.scrollContent}
          contentContainerStyle={styles.content}
          showsVerticalScrollIndicator={false}
          keyboardShouldPersistTaps="handled"
        >
          {/* Progress Indicator - only show in onboarding mode */}
          {mode === 'onboarding' && (
            <ProgressIndicator currentStep={currentStep} totalSteps={totalSteps} />
          )}

          {/* Back Button - Only show in onboarding mode */}
          {mode === 'onboarding' && onBack && (
            <TouchableOpacity style={styles.backButton} onPress={onBack} activeOpacity={0.7}>
              <Ionicons name="arrow-back" size={24} color={colors.text} />
            </TouchableOpacity>
          )}

          {/* Sport Badge */}
          <View style={[styles.sportBadge, { backgroundColor: colors.primary }]}>
            <Text style={[styles.sportBadgeText, { color: colors.primaryForeground }]}>
              {t('onboarding.tennis' as TranslationKey)}
            </Text>
          </View>

          {/* Subtitle with NTRP link */}
          <Text style={[styles.subtitle, { color: colors.textMuted }]}>
            {mode === 'edit' ? (
              <Text
                style={[styles.link, { color: colors.primary }]}
                onPress={() =>
                  Linking.openURL(
                    'https://www.usta.com/en/home/improve/national-tennis-rating-program.html'
                  )
                }
              >
                {t('onboarding.ratingOverlay.learnMoreNtrp' as TranslationKey)}
              </Text>
            ) : (
              t('onboarding.ratingOverlay.tennisSubtitle' as TranslationKey)
            )}
          </Text>

          {/* Rating Options */}
          {isLoading ? (
            <View style={styles.loadingContainer}>
              <ActivityIndicator size="large" color={colors.primary} />
              <Text style={[styles.loadingText, { color: colors.textMuted }]}>
                {t('onboarding.ratingOverlay.loading' as TranslationKey)}
              </Text>
            </View>
          ) : (
            <View style={styles.ratingGrid}>
              {ratings.map(rating => (
                <TouchableOpacity
                  key={rating.id}
                  style={[
                    styles.ratingCard,
                    { backgroundColor: colors.inputBackground },
                    selectedRating === rating.id && [
                      styles.ratingCardSelected,
                      {
                        borderColor: colors.primary,
                        backgroundColor: isDark ? colors.inputBackground : primary[50],
                      },
                    ],
                  ]}
                  onPress={() => {
                    selectionHaptic();
                    setSelectedRating(rating.id);
                  }}
                  activeOpacity={0.8}
                >
                  <View style={{ flexDirection: 'row', alignItems: 'center', marginBottom: 4 }}>
                    <Ionicons
                      name={getRatingIcon(getSkillCategory(rating.score_value))}
                      size={20}
                      color={colors.primary}
                      style={{ marginRight: 8 }}
                    />
                    <Text
                      style={[
                        styles.ratingLevel,
                        {
                          color: selectedRating === rating.id ? colors.text : colors.text,
                        },
                      ]}
                    >
                      {t(getNtrpSkillLabelKey(rating.score_value) as TranslationKey)}
                    </Text>
                  </View>
                  <Text
                    style={[
                      styles.ratingNtrp,
                      {
                        color: selectedRating === rating.id ? colors.text : colors.textMuted,
                      },
                    ]}
                  >
                    {rating.display_label}
                  </Text>
                  <Text
                    style={[
                      styles.ratingDescription,
                      {
                        color: selectedRating === rating.id ? colors.text : colors.textMuted,
                      },
                    ]}
                  >
                    {t(getNtrpDescriptionKey(rating.score_value) as TranslationKey)}
                  </Text>
                </TouchableOpacity>
              ))}
            </View>
          )}
        </ScrollView>

        {/* Sticky Footer */}
        <View style={[styles.footer, { borderTopColor: colors.border }]}>
          <TouchableOpacity
            style={[
              styles.submitButton,
              { backgroundColor: colors.primary },
              (!selectedRating || isSaving) && { opacity: 0.6 },
            ]}
            onPress={handleContinue}
            disabled={!selectedRating || isSaving}
            activeOpacity={0.8}
          >
            {isSaving ? (
              <ActivityIndicator size="small" color={colors.primaryForeground} />
            ) : (
              <Text weight="semibold" style={{ color: colors.primaryForeground }}>
                {mode === 'edit'
                  ? t('onboarding.ratingOverlay.save' as TranslationKey)
                  : t('onboarding.ratingOverlay.continue' as TranslationKey)}
              </Text>
            )}
          </TouchableOpacity>
        </View>
      </View>
    </ActionSheet>
  );
}

// Keep old export for backwards compatibility during migration
const TennisRatingOverlay: React.FC<TennisRatingOverlayProps> = ({
  visible,
  onClose,
  onBack,
  onContinue,
  currentStep,
  totalSteps,
  mode,
  initialRating,
  onSave,
}) => {
  useEffect(() => {
    if (visible) {
      SheetManager.show('tennis-rating', {
        payload: {
          mode,
          initialRating,
          onSave,
          onContinue,
          onBack,
          currentStep,
          totalSteps,
        },
      });
    }
  }, [visible, mode, initialRating, onSave, onContinue, onBack, currentStep, totalSteps]);

  useEffect(() => {
    if (!visible) {
      SheetManager.hide('tennis-rating');
    }
  }, [visible]);

  return null;
};

